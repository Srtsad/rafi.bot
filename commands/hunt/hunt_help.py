from aiogram import Router, types

router = Router()

@router.message(lambda m: m.text and m.text.lower() in ["охота помощь", "hunt help"])
async def hunt_help(message: types.Message):
    text = (
        "🏹 ОХОТА\n"
        "━━━━━━━━━━━━━━\n\n"
        "🔘 Команды охоты:\n"
        "  🌲 Охота — открывай охотничье меню и выдвигайся в лес\n"
        "  🎯 Стрелять — целься и бей точно в цель\n"
        "  💰 Продать [назв.добычи] - кол-во — обменивай трофеи на золото\n"
        "  🥷 Купить [патроны/вещи] — не забудь пополнить боезапас\n"
        "  🛒 Лавка — патроны, улучшения и курс добычи в одном месте\n"
        "  🏆 охота топ — покажи всем, кто настоящий охотник\n\n"
        "💡 Советы по охоте:\n"
        "  🦄 Редкая и эпическая добыча повышает твоё место в топе — стань легендой\n"
        "  ⚡ Улучшай оружие и прицел — шанс на редкую и эпическую добычу вырастет\n"
        "  🌙 Ночная охота (18:00–6:00) — тайное время трофеев\n"
        "  🔥 Сохраняй патроны — чем их больше, тем круче трофеи\n"
        "  💬 Помни: настоящий охотник не бежит за добычей — добыча сама идёт к меткому стрелку\n"
    )
    await message.answer(text)
