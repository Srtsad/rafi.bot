# ---------------------------
# Проверка возможности копать
# ---------------------------
def can_mine(player):
    """
    Проверяет, может ли игрок копать.
    Возвращает: (True/False, причина)
    """
    if player.get("energy", 0) < ENERGY_COST:
        return False, "no_energy"
    now = time.time()
    if now - player.get("last_mine", 0) < COOLDOWN:
        return False, "cooldown"
    return True, None

# ---------------------------
# Функция добычи
# ---------------------------
def mine(player):
    """
    Генерирует добычу для игрока на основе шансов из DROPS_CONFIG.
    Возвращает текст события и словарь ресурсов.
    """
    roll = random.randint(1, 100)
    current = 0

    for drop in DROPS_CONFIG:
        current += drop["chance"]
        if roll <= current:
            ores = drop["ores_func"]()
            text = drop["text"].format(**ores)
            return text, ores

    # На всякий случай пустая порода
    return "🌫 Пустая порода\n\nНичего не найдено.\n⚡ Энергия −10", {}
